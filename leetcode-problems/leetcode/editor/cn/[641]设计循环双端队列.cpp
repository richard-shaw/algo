class MyCircularDeque {
private:
    struct Node {
        int val;
        Node *pre, *nxt;
        Node(int val) : val(val), pre(NULL), nxt(NULL) {}
    };
    Node *head, *tail;
    int size, maxSize;
public:
    MyCircularDeque(int k) {
        this->maxSize = k;
        this->head = this->tail = NULL;
        this->size = 0;
    }

    bool insertFront(int value) {
        if (this->isFull()) return false;
        size++;
        Node *node = new Node(value);
        if (head == NULL) head = tail = node;
        else {
            node->nxt = head;
            head->pre = node;
            head = node;
        }
        return true;
    }

    bool insertLast(int value) {
        if (this->isFull()) return false;
        size++;
        Node *node = new Node(value);
        if (tail == NULL) head = tail = node;
        else {
            tail->nxt = node;
            node->pre = tail;
            tail = node;
        }
        return true;
    }

    bool deleteFront() {
        if (this->isEmpty()) return false;
        size--;
        if (head == tail) head = tail = NULL;
        else {
            head = head->nxt;
            delete head->pre;
            head->pre = NULL;
        }
        return true;
    }

    bool deleteLast() {
        if (this->isEmpty()) return false;
        size--;
        if (head == tail) head = tail = NULL;
        else {
            tail = tail->pre;
            delete tail->nxt;
            tail->nxt = NULL;
        }
        return true;
    }

    int getFront() {
        if (this->isEmpty()) return -1;
        return head->val;
    }

    int getRear() {
        if (this->isEmpty()) return -1;
        return tail->val;
    }

    bool isEmpty() {
        return this->size == 0;
    }

    bool isFull() {
        return this->size == this->maxSize;
    }
};

